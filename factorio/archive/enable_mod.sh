#!/bin/bash
# Script to enable FV Embodied Agent mod in mod-list.json

echo "Creating/enabling mod-list.json for FV Embodied Agent mod..."
echo ""
echo "Run these commands on TrueNAS:"
echo ""
echo "# Check if file exists"
echo "cat /mnt/boot-pool/apps/factorio/mods/mod-list.json 2>/dev/null || echo 'File does not exist'"
echo ""
echo "# Create mod-list.json with mod enabled"
echo "cat > /mnt/boot-pool/apps/factorio/mods/mod-list.json << 'EOF'"
echo "{"
echo "  \"mods\": ["
echo "    {"
echo "      \"name\": \"fv_embodied_agent\","
echo "      \"enabled\": true"
echo "    }"
echo "  ]"
echo "}"
echo "EOF"
echo ""
echo "# Set permissions"
echo "sudo chown apps:apps /mnt/boot-pool/apps/factorio/mods/mod-list.json"
echo ""
echo "# Restart Factorio"
echo "cd /mnt/boot-pool/apps/factorio && sudo docker compose restart"
